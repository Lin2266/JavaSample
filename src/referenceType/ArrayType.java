package referenceType;
//陣列宣告
public class ArrayType {
/*
 陣列指的是一連串資料或物件，在Java語言中同一個陣列只能存放相同資料型態的資料，不論是基本資料型態或者是類別型態資料，
 皆可宣告成為陣列，在陣列中[]代表的是索引運算子，用來指向陣列物件中每個元素的位置或者是物件陣列宣告的長度。
 */
	public static void main(String[] args) {
		//類別型態不合法的宣告
		//[] String s; //編譯錯誤，類別跟基本型態合法與不合法宣告都一樣
		//基本型態陣列合法的宣告
		int i[];
		int i0 [];
		int[] i1;
		int [] i2;
		int []i3;
		

	}

}
